// Current Language State
let currentLang = localStorage.getItem('lang') || 'pl';

// Apply Language Function
function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);

  // Update document language attribute
  document.documentElement.lang = lang;

  // Apply data-i18n (text content)
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (i18n[lang] && i18n[lang][key]) {
      el.textContent = i18n[lang][key];
    }
  });

  // Apply data-i18n-ph (placeholder)
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (i18n[lang] && i18n[lang][key]) {
      el.placeholder = i18n[lang][key];
    }
  });

  // Apply data-i18n-href (href)
  document.querySelectorAll('[data-i18n-href]').forEach(el => {
    const key = el.getAttribute('data-i18n-href');
    if (i18n[lang] && i18n[lang][key]) {
      el.href = i18n[lang][key];
    }
  });

  // Update language toggle button active states
  document.querySelectorAll('[data-lang]').forEach(btn => {
    btn.classList.remove('active');
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('active');
    }
  });

  // Reinitialise typewriter with new language words (BUG-03 fix)
  initTypewriter();
}

// Initialize Navbar
function initNavbar() {
  const navbar = document.querySelector('.navbar');
  const hamburger = document.querySelector('.hamburger');
  const mobileMenu = document.querySelector('.mobile-menu');
  const langBtns = document.querySelectorAll('[data-lang]');

  // Scroll listener for navbar.scrolled class
  if (navbar) {
    window.addEventListener('scroll', () => {
      if (window.scrollY > 0) {
        navbar.classList.add('scrolled');
      } else {
        navbar.classList.remove('scrolled');
      }
    });
  }

  // Hamburger menu toggle
  if (hamburger && mobileMenu) {
    hamburger.addEventListener('click', () => {
      mobileMenu.classList.toggle('open');
    });

    // Close menu when clicking on a link
    mobileMenu.querySelectorAll('a').forEach(link => {
      link.addEventListener('click', () => {
        mobileMenu.classList.remove('open');
      });
    });
  }

  // Language toggle buttons
  langBtns.forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      const lang = btn.getAttribute('data-lang');
      applyLang(lang);
    });
  });
}

// Initialize Scroll Animations
function initScrollAnimations() {
  const fadeElements = document.querySelectorAll('.fade-up');

  if (fadeElements.length === 0) return;

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  fadeElements.forEach(el => {
    observer.observe(el);
  });
}

// Animate Counter Function
function animateCounter(el, target, duration = 1800) {
  let start = 0;
  const startTime = performance.now();

  function cubicEaseOut(t) {
    return 1 - Math.pow(1 - t, 3);
  }

  function frame(currentTime) {
    const elapsed = currentTime - startTime;
    const progress = Math.min(elapsed / duration, 1);
    const easeProgress = cubicEaseOut(progress);
    const current = Math.floor(start + (target - start) * easeProgress);

    el.textContent = current.toLocaleString();

    if (progress < 1) {
      requestAnimationFrame(frame);
    }
  }

  requestAnimationFrame(frame);
}

// Initialize Counters
function initCounters() {
  const counterElements = document.querySelectorAll('[data-counter]');

  if (counterElements.length === 0) return;

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const target = parseInt(entry.target.getAttribute('data-counter'), 10);
        animateCounter(entry.target, target);
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.5 });

  counterElements.forEach(el => {
    observer.observe(el);
  });
}

// Typewriter Interval Storage
let _typewriterInterval = null;

// Initialize Contact Form
function initContactForm() {
  const form = document.getElementById('contactForm');
  const formSuccess = document.getElementById('formSuccess');

  if (!form) return;

  form.addEventListener('submit', async (e) => {
    e.preventDefault();

    // Validate required fields
    const requiredFields = form.querySelectorAll('[required]');
    let isValid = true;

    requiredFields.forEach(field => {
      const errorEl = field.nextElementSibling;
      if (!field.value.trim()) {
        isValid = false;
        field.style.borderColor = '#d32f2f';
        if (errorEl && errorEl.classList.contains('form-error')) {
          errorEl.style.display = 'block';
        }
      } else if (field.type === 'email' && !field.value.match(/.+@.+\..+/)) {
        isValid = false;
        field.style.borderColor = '#d32f2f';
        if (errorEl && errorEl.classList.contains('form-error')) {
          errorEl.style.display = 'block';
        }
      } else {
        field.style.borderColor = '';
        if (errorEl && errorEl.classList.contains('form-error')) {
          errorEl.style.display = 'none';
        }
      }
    });

    if (!isValid) return;

    // Submit to Netlify Forms (BUG-01 fix)
    try {
      await fetch('/', {
        method: 'POST',
        headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
        body: new URLSearchParams(new FormData(form)).toString()
      });
    } catch (err) {
      // Network error — fail silently; Netlify dashboard will show missing submissions
      console.error('Form submission error:', err);
    }

    // Show success, hide form (BUG-04 fix — auto-dismiss after 6s)
    form.style.display = 'none';
    form.reset();
    if (formSuccess) {
      formSuccess.style.display = 'block';
      formSuccess.scrollIntoView({ behavior: 'smooth', block: 'center' });
    }

    const autoHide = setTimeout(() => {
      if (formSuccess) formSuccess.style.display = 'none';
      form.style.display = 'block';
    }, 6000);

    // Manual dismiss button (BUG-04 fix)
    const dismissBtn = document.getElementById('formSuccessDismiss');
    if (dismissBtn) {
      dismissBtn.onclick = () => {
        clearTimeout(autoHide);
        formSuccess.style.display = 'none';
        form.style.display = 'block';
      };
    }
  });
}

// Initialize Typewriter Effect
function initTypewriter() {
  const typewriterEl = document.getElementById('typewriter');

  if (!typewriterEl) return;

  const wordLists = {
    pl: ['studentów', 'reprezentacji', 'zmian', 'społeczności'],
    en: ['students', 'representation', 'change', 'community']
  };

  let wordIndex = 0;
  const cycleInterval = 2800;
  const fadeTransitionDuration = 300;

  function updateWord() {
    const words = wordLists[currentLang];
    const nextWord = words[wordIndex % words.length];

    typewriterEl.style.opacity = '0';
    typewriterEl.style.transition = `opacity ${fadeTransitionDuration}ms ease-in-out`;

    setTimeout(() => {
      typewriterEl.textContent = nextWord;
      typewriterEl.style.opacity = '1';
      wordIndex++;
    }, fadeTransitionDuration);
  }

  // Initial word
  if (wordLists[currentLang]) {
    typewriterEl.textContent = wordLists[currentLang][0];
    typewriterEl.style.opacity = '1';
    typewriterEl.style.transition = `opacity ${fadeTransitionDuration}ms ease-in-out`;
  }

  // Cycle through words
  if (_typewriterInterval) clearInterval(_typewriterInterval);
  _typewriterInterval = setInterval(updateWord, cycleInterval);
}

// DOMContentLoaded Listener
document.addEventListener('DOMContentLoaded', () => {
  initNavbar();
  initScrollAnimations();
  initCounters();
  initContactForm();
  initTypewriter();
  applyLang(currentLang);
});
